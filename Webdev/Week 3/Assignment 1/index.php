       <h1>Simple Omelette Recipe</h1> 
       <p style="text-align: left;" id="padding1">
       <br>An easy and quick dish, perfect for any meal. This classic omelette
       combines beaten eggs <br> cooked to perfection, optionally filled with your choice of cheese
       , vegetables, or meats.
       </p>

       <div id="preptime"> <p class="red111"> Preparation time </p>
       <ul id="list11">
           <li class="spaceleft"><b> Total: </b> Approximately 10 minutes </li>
           <li class="spaceleft"><b> Preparation: </b> 5 minutes </li>
           <li class="spaceleft"><b> Cooking: </b> 5 minutes </li>
       </ul> 
</div>

       <br><div class="ing1"> Ingredients  </div>

       <ul style="list-style-type:square">
           <li class="spaceleft">2-3 large eggs</li>
           <li class="spaceleft">Salt, to taste</li>
           <li class="spaceleft">Pepper, to taste</li>
           <li class="spaceleft">1 tablespoon of butter or oil</li>
           <li class="spaceleft">Optional fillings: cheese, diced vegetables, cooked meats, herbs</li><br>
       </ul>  
 
    <hr class="solid">
    

       <br><div class="ing1"> Instructions  </div>
          <ol>
             <li><b>Beat the eggs:</b>In a bowl, beat the eggs with a pinch of salt and pepper until they are<br>well mixed. You can add a tablespoon of water or milk for a fluffier texture.</li>
             <li><b>Heat the pan:</b>Place a non-stick frying pan over medium heat and add butter or oil.
                 pour in the eggs. Tilt the pan to ensure the eggs evenly coat the surface.</li>
             <li><b>Cook the omelette:</b>Once the butter is melted and bubbling, pour in the eggs.
                 Tilt the<br> pan to ensure the eggs evenly coat the surface.</li>
             <li><b>Add fillings (optional):</b>When the eggs begin to set at the edges but are still slightly<br> runny in the middle,
                 sprinkle your chosen fillings over one half of the omelette.</li>
             <li><b>Fold and serve:</b>As the omelette continues to cook, carefully lift one edge and fold it<br> over the fillings.
                 Let it cook for another minute, then slide it onto a plate.</li>
             <li><b>Enjoy:</b>Serve hot, with additional salt and pepper if needed.</li><br>
          </ol>

